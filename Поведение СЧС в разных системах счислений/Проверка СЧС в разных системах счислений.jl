using Printf # –ø–æ–¥–∫–ª—é—á–∞–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ –≤—ã–≤–æ–¥–∞ —á–∏—Å–µ–ª
using CSV # –ø–æ–¥–∫–ª—é—á–∞–µ–º –º–æ–¥—É–ª—å –¥–ª—è –∑–∞–ø–∏—Å–∏ –∏ —á—Ç–µ–Ω–∏—è CSV-—Ñ–∞–π–ª–æ–≤
using DataFrames # –ø–æ–¥–∫–ª—é—á–∞–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ç–∞–±–ª–∏—á–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
using Base.Threads # –ø–æ–¥–∫–ª—é—á–∞–µ–º –º–Ω–æ–≥–æ–ø–æ—Ç–æ—á–Ω–æ—Å—Ç—å (–¥–ª—è –∞—Ç–æ–º–∞—Ä–Ω—ã—Ö —Å—á—ë—Ç—á–∏–∫–æ–≤)
using ProgressMeter # –ø–æ–¥–∫–ª—é—á–∞–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –≤ —Å—Ç—Ä–æ–∫—É –≤ –∑–∞–¥–∞–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è (–±–µ–∑ –ø—Ä–µ—Ñ–∏–∫—Å–∞, –Ω–∞–ø—Ä–∏–º–µ—Ä "FF" –¥–ª—è 255 –≤ base=16)
function string_in_base(n::Integer, base::Int) # —Ñ—É–Ω–∫—Ü–∏—è: –ø–µ—Ä–µ–≤–µ—Å—Ç–∏ —á–∏—Å–ª–æ n –≤ —Å—Ç—Ä–æ–∫—É –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è base
if n == 0 # –µ—Å–ª–∏ —á–∏—Å–ª–æ —Ä–∞–≤–Ω–æ –Ω—É–ª—é
return "0" # —Å—Ä–∞–∑—É –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å—Ç—Ä–æ–∫—É "0"
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è
digits = "" # —Å—é–¥–∞ –±—É–¥–µ–º —Å–æ–±–∏—Ä–∞—Ç—å —Ü–∏—Ñ—Ä—ã —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ (–≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ!)
num = abs(n) # —Ä–∞–±–æ—Ç–∞–µ–º —Å –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º —á–∏—Å–ª–æ–º, –¥–∞–∂–µ –µ—Å–ª–∏ –≤—Ö–æ–¥ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–π
while num > 0 # –ø–æ–∫–∞ —á–∏—Å–ª–æ –Ω–µ —Å—Ç–∞–ª–æ –Ω—É–ª—ë–º
r = num % base # –±–µ—Ä—ë–º –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è –Ω–∞ –æ—Å–Ω–æ–≤–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã
# –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ü–∏—Ñ—Ä—ã 0‚Äì9, –∑–∞—Ç–µ–º A‚ÄìZ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ (–¥–æ base=36)
digit_char = r < 10 ? Char('0' + r) : Char('A' + (r - 10)) # –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –æ—Å—Ç–∞—Ç–æ–∫ –≤ —Å–∏–º–≤–æ–ª: '0'-'9' –∏–ª–∏ 'A'-'Z'
digits = digit_char * digits # –¥–æ–±–∞–≤–ª—è–µ–º —Å–∏–º–≤–æ–ª –°–õ–ï–í–ê (–ø–æ—Ç–æ–º—É —á—Ç–æ —Ä–∞–∑–±–∏—Ä–∞–µ–º —á–∏—Å–ª–æ —Å –∫–æ–Ω—Ü–∞)
num √∑= base # –¥–µ–ª–∏–º —á–∏—Å–ª–æ –Ω–∞—Ü–µ–ª–æ –Ω–∞ –æ—Å–Ω–æ–≤–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã
end # –∫–æ–Ω–µ—Ü —Ü–∏–∫–ª–∞ while
return digits # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≥–æ—Ç–æ–≤—É—é —Å—Ç—Ä–æ–∫—É –±–µ–∑ –≤–µ–¥—É—â–∏—Ö –Ω—É–ª–µ–π
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –û–±—Ä–∞—Ç–Ω–æ–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ: —Å—Ç—Ä–æ–∫–∞ –≤ —á–∏—Å–ª–µ –≤ –∑–∞–¥–∞–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ ‚Üí BigInt
function parse_from_base(s::String, base::Int)::BigInt # —Ñ—É–Ω–∫—Ü–∏—è: –ø–µ—Ä–µ–≤–µ—Å—Ç–∏ —Å—Ç—Ä–æ–∫—É s –∏–∑ —Å–∏—Å—Ç–µ–º—ã base –≤ –±–æ–ª—å—à–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
if s == "0" || all(c -> c == '0', s) # –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ ‚Äî —ç—Ç–æ "0" –∏–ª–∏ —Å–ø–ª–æ—à–Ω—ã–µ –Ω—É–ª–∏
return BigInt(0) # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–æ–ª—å –∫–∞–∫ –±–æ–ª—å—à–æ–µ —Ü–µ–ª–æ–µ
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è
value = BigInt(0) # –Ω–∞—á–∏–Ω–∞–µ–º —Å—Ç—Ä–æ–∏—Ç—å —á–∏—Å–ª–æ —Å –Ω—É–ª—è
for c in s # –ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ –∫–∞–∂–¥–æ–º—É —Å–∏–º–≤–æ–ª—É —Å—Ç—Ä–æ–∫–∏ —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ
d = if '0' <= c <= '9' # –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª ‚Äî —Ü–∏—Ñ—Ä–∞ –æ—Ç '0' –¥–æ '9'
Int(c - '0') # –≤—ã—á–∏—Å–ª—è–µ–º –µ—ë —á–∏—Å–ª–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ (0‚Äì9)
elseif 'A' <= c <= 'Z' # –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª ‚Äî –∑–∞–≥–ª–∞–≤–Ω–∞—è –±—É–∫–≤–∞ A‚ÄìZ
Int(c - 'A' + 10) # –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –≤ —á–∏—Å–ª–æ 10‚Äì35
elseif 'a' <= c <= 'z' # –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª ‚Äî —Å—Ç—Ä–æ—á–Ω–∞—è –±—É–∫–≤–∞ a‚Äìz
Int(c - 'a' + 10) # —Ç–æ–∂–µ –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –≤ 10‚Äì35
else # –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª –≤–æ–æ–±—â–µ –Ω–µ —Ü–∏—Ñ—Ä–∞ –∏ –Ω–µ –ª–∞—Ç–∏–Ω—Å–∫–∞—è –±—É–∫–≤–∞
error("–ù–µ–¥–æ–ø—É—Å—Ç–∏–º—ã–π —Å–∏–º–≤–æ–ª '$c' –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è $base") # –≤—ã–¥–∞—ë–º –æ—à–∏–±–∫—É
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è –≤—ã–±–æ—Ä–∞ —Å–∏–º–≤–æ–ª–∞
if d >= base # –µ—Å–ª–∏ —Ü–∏—Ñ—Ä–∞ –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–∞ –æ—Å–Ω–æ–≤–∞–Ω–∏—é —Å–∏—Å—Ç–µ–º—ã
error("–¶–∏—Ñ—Ä–∞ '$c' –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–∞ –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è $base") # –≤—ã–¥–∞—ë–º –æ—à–∏–±–∫—É
end # –∫–æ–Ω–µ—Ü –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–æ–ø—É—Å—Ç–∏–º–æ—Å—Ç–∏ —Ü–∏—Ñ—Ä—ã
value = value * base + d # —É–º–Ω–æ–∂–∞–µ–º —Ç–µ–∫—É—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–∞ –æ—Å–Ω–æ–≤–∞–Ω–∏–µ –∏ –ø—Ä–∏–±–∞–≤–ª—è–µ–º –Ω–æ–≤—É—é —Ü–∏—Ñ—Ä—É
end # –∫–æ–Ω–µ—Ü —Ü–∏–∫–ª–∞ –ø–æ —Å–∏–º–≤–æ–ª–∞–º
return value # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫–∞–∫ –±–æ–ª—å—à–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –†–∞–∑–±–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —á–∏—Å–ª–∞ –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è `base` –Ω–∞ m —á–∞—Å—Ç–µ–π
function split_number_str_base(s::String, m::Int) # —Ñ—É–Ω–∫—Ü–∏—è: —Ä–∞–∑–±–∏—Ç—å —Å—Ç—Ä–æ–∫—É s –Ω–∞ m –ø–æ—á—Ç–∏ —Ä–∞–≤–Ω—ã—Ö —á–∞—Å—Ç–µ–π
len = length(s) # —Å–∫–æ–ª—å–∫–æ –≤—Å–µ–≥–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ
if len == 1 && m > 1 # –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –∏–∑ –æ–¥–Ω–æ–≥–æ —Å–∏–º–≤–æ–ª–∞, –∞ –Ω—É–∂–Ω–æ –º–Ω–æ–≥–æ —á–∞—Å—Ç–µ–π
s = lpad(s, m, '0')  # –¥–æ–ø–æ–ª–Ω—è–µ–º –¥–æ m —Å–∏–º–≤–æ–ª–æ–≤ –Ω—É–ª—è–º–∏ —Å–ª–µ–≤–∞
len = m # –æ–±–Ω–æ–≤–ª—è–µ–º –¥–ª–∏–Ω—É —Å—Ç—Ä–æ–∫–∏
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è

base_len = div(len, m) # —Å–∫–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª–æ–≤ –º–∏–Ω–∏–º—É–º –≤ –∫–∞–∂–¥–æ–π —á–∞—Å—Ç–∏
remainder = len % m # —Å–∫–æ–ª—å–∫–æ —á–∞—Å—Ç–µ–π –ø–æ–ª—É—á–∞—Ç –µ—â—ë –ø–æ –æ–¥–Ω–æ–º—É —Å–∏–º–≤–æ–ª—É
parts = String[] # —Å–æ–∑–¥–∞—ë–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –¥–ª—è —á–∞—Å—Ç–µ–π
idx = 1 # —Å –∫–∞–∫–æ–π –ø–æ–∑–∏—Ü–∏–∏ –≤ —Å—Ç—Ä–æ–∫–µ –Ω–∞—á–∏–Ω–∞—Ç—å –±—Ä–∞—Ç—å —á–∞—Å—Ç—å
for i in 1:m # –ø–æ–≤—Ç–æ—Ä—è–µ–º m —Ä–∞–∑ (–ø–æ —á–∏—Å–ª—É —á–∞—Å—Ç–µ–π)
current_len = base_len + (i <= remainder ? 1 : 0) # –µ—Å–ª–∏ —á–∞—Å—Ç—å –ø–æ–ø–∞–¥–∞–µ—Ç –≤ "–æ—Å—Ç–∞—Ç–æ–∫", –¥–æ–±–∞–≤–ª—è–µ–º 1 —Å–∏–º–≤–æ–ª
push!(parts, s[idx:idx+current_len-1]) # –≤—ã—Ä–µ–∑–∞–µ–º –∫—É—Å–æ–∫ –∏ –¥–æ–±–∞–≤–ª—è–µ–º –≤ —Å–ø–∏—Å–æ–∫
idx += current_len # —Å–¥–≤–∏–≥–∞–µ–º—Å—è –≤–ø—Ä–∞–≤–æ –Ω–∞ –¥–ª–∏–Ω—É –≤–∑—è—Ç–æ–π —á–∞—Å—Ç–∏
end # –∫–æ–Ω–µ—Ü —Ü–∏–∫–ª–∞ –ø–æ —á–∞—Å—Ç—è–º
return parts # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–ø–∏—Å–æ–∫ —á–∞—Å—Ç–µ–π
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –£–º–Ω–æ–∂–∞–µ—Ç —á–∞—Å—Ç—å (–≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏ –≤ —Å–∏—Å—Ç–µ–º–µ base) –Ω–∞ k, —Å–æ—Ö—Ä–∞–Ω—è—è –¥–ª–∏–Ω—É
function multiply_preserve_length_base(part::String, k::Int, base::Int)::String # —Ñ—É–Ω–∫—Ü–∏—è: —É–º–Ω–æ–∂–∏—Ç—å —á–∞—Å—Ç—å –Ω–∞ k –∏ –≤–µ—Ä–Ω—É—Ç—å —Å—Ç—Ä–æ–∫—É —Ç–æ–π –∂–µ –¥–ª–∏–Ω—ã (—Å –Ω—É–ª—è–º–∏ —Å–ª–µ–≤–∞ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏)
if all(c -> c == '0', part) # –µ—Å–ª–∏ –≤—Å—è —á–∞—Å—Ç—å —Å–æ—Å—Ç–æ–∏—Ç –∏–∑ –Ω—É–ª–µ–π
return lpad("", length(part), '0')  # –æ—Å—Ç–∞—ë–º—Å—è –∏–∑ –Ω—É–ª–µ–π —Ç–æ–π –∂–µ –¥–ª–∏–Ω—ã
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è
num = parse_from_base(part, base) * k # –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º —á–∞—Å—Ç—å –≤ —á–∏—Å–ª–æ, —É–º–Ω–æ–∂–∞–µ–º –Ω–∞ k
result_str = string_in_base(num, base) # –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É –≤ –Ω—É–∂–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ
return lpad(result_str, length(part), '0') # –¥–æ–±–∞–≤–ª—è–µ–º –Ω—É–ª–∏ —Å–ª–µ–≤–∞, –µ—Å–ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫–æ—Ä–æ—á–µ –∏—Å—Ö–æ–¥–Ω–æ–π —á–∞—Å—Ç–∏
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –£–¥–∞–ª—è–µ—Ç –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ (–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω —Å–∏–º–≤–æ–ª)
function remove_leading_zeros(s::String)::String # —Ñ—É–Ω–∫—Ü–∏—è: —É–±—Ä–∞—Ç—å –≤—Å–µ –Ω—É–ª–∏ –≤ –Ω–∞—á–∞–ª–µ —Å—Ç—Ä–æ–∫–∏, –Ω–æ –æ—Å—Ç–∞–≤–∏—Ç—å —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω —Å–∏–º–≤–æ–ª
if all(c -> c == '0', s) # –µ—Å–ª–∏ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã ‚Äî –Ω—É–ª–∏
return "0" # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º "0"
else # –∏–Ω–∞—á–µ
idx = findfirst(c -> c != '0', s) # –Ω–∞—Ö–æ–¥–∏–º –ø–µ—Ä–≤—É—é –ù–ï-–Ω—É–ª–µ–≤—É—é —Ü–∏—Ñ—Ä—É
return isnothing(idx) ? "0" : s[idx:end] # –µ—Å–ª–∏ –Ω–∞—à–ª–∏ ‚Äî —Ä–µ–∂–µ–º —Å—Ç—Ä–æ–∫—É; –µ—Å–ª–∏ –Ω–µ—Ç ‚Äî "0"
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç –¥–≤–µ —Å—Ç—Ä–æ–∫–∏ (–≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è) –ø–æ –Ω–∞—á–∞–ª—É –∏ –∫–æ–Ω—Ü—É
function compare_pq_nk(pq::String, nk::String) # —Ñ—É–Ω–∫—Ü–∏—è: —Å—Ä–∞–≤–Ω–∏—Ç—å –¥–≤–µ —Å—Ç—Ä–æ–∫–∏ –∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ç–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è (–ø–æ–ª–Ω–æ–µ, –Ω–∞—á–∞–ª–æ, –∫–æ–Ω–µ—Ü, –æ–±–∞ –∫–æ–Ω—Ü–∞ –∏–ª–∏ –Ω–∏—á–µ–≥–æ)
if pq == nk # –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∏ –ø–æ–ª–Ω–æ—Å—Ç—å—é —Å–æ–≤–ø–∞–¥–∞—é—Ç
return "‚úÖ –ü–æ–ª–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ" # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –ø–æ–ª–Ω–æ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–∏
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è

min_len = min(length(pq), length(nk)) # —Å–∫–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª–æ–≤ –º–æ–∂–Ω–æ —Å—Ä–∞–≤–Ω–∏—Ç—å (–¥–ª–∏–Ω–∞ –∫–æ—Ä–æ—Ç–∫–æ–π —Å—Ç—Ä–æ–∫–∏)
prefix_match = 0 # —Å—á—ë—Ç—á–∏–∫ —Å–æ–≤–ø–∞–¥–∞—é—â–∏—Ö —Ü–∏—Ñ—Ä –≤ –Ω–∞—á–∞–ª–µ
for i in 1:min_len # –∏–¥—ë–º –æ—Ç –ø–µ—Ä–≤–æ–π —Ü–∏—Ñ—Ä—ã
pq[i] == nk[i] ? prefix_match += 1 : break # –µ—Å–ª–∏ —Ü–∏—Ñ—Ä—ã —Å–æ–≤–ø–∞–ª–∏ ‚Äî +1, –∏–Ω–∞—á–µ –≤—ã–π—Ç–∏ –∏–∑ —Ü–∏–∫–ª–∞
end # –∫–æ–Ω–µ—Ü —Ü–∏–∫–ª–∞ –ø–æ –ø—Ä–µ—Ñ–∏–∫—Å—É

suffix_match = 0 # —Å—á—ë—Ç—á–∏–∫ —Å–æ–≤–ø–∞–¥–∞—é—â–∏—Ö —Ü–∏—Ñ—Ä –≤ –∫–æ–Ω—Ü–µ
for i in 1:min_len # –∏–¥—ë–º –æ—Ç –ø–æ—Å–ª–µ–¥–Ω–µ–π —Ü–∏—Ñ—Ä—ã (–ø–æ –∏–Ω–¥–µ–∫—Å—É —Å –∫–æ–Ω—Ü–∞)
pq[end - i + 1] == nk[end - i + 1] ? suffix_match += 1 : break # –µ—Å–ª–∏ —Å–æ–≤–ø–∞–ª–∏ ‚Äî +1, –∏–Ω–∞—á–µ –≤—ã–π—Ç–∏
end # –∫–æ–Ω–µ—Ü —Ü–∏–∫–ª–∞ –ø–æ —Å—É—Ñ—Ñ–∏–∫—Å—É

if prefix_match > 0 && suffix_match > 0 # –µ—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞—é—Ç –∏ –Ω–∞—á–∞–ª–æ, –∏ –∫–æ–Ω–µ—Ü
return "üîÑ –°–æ–≤–ø–∞–¥–∞—é—Ç –Ω–∞—á–∞–ª–æ –∏ –∫–æ–Ω–µ—Ü" # —ç—Ç–æ –°–ß–°-—è–≤–ª–µ–Ω–∏–µ
elseif prefix_match > 0 # –µ—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ
return "üîÑ –°–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ" # —á–∞—Å—Ç–∏—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
elseif suffix_match > 0 # –µ—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü
return "üîÑ –°–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü" # —á–∞—Å—Ç–∏—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
else # –µ—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ —Å–æ–≤–ø–∞–ª–æ
return "‚ùå –ù–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π" # –Ω–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
end # –∫–æ–Ω–µ—Ü —É—Å–ª–æ–≤–∏—è –≤—ã–±–æ—Ä–∞ —Ç–∏–ø–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –°–ß–° –¥–ª—è –æ–¥–Ω–æ–≥–æ N –≤ –∑–∞–¥–∞–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è
function check_algoritm_base(N::Integer, m::Int, k::Int, base::Int) # —Ñ—É–Ω–∫—Ü–∏—è: –ø—Ä–æ–≤–µ—Å—Ç–∏ –ø–æ–ª–Ω—ã–π –∞–Ω–∞–ª–∏–∑ –°–ß–° –¥–ª—è —á–∏—Å–ª–∞ N –≤ —Å–∏—Å—Ç–µ–º–µ base —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏ m –∏ k
N_str = string_in_base(N, base) # –∏—Å—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞ –≤ —Å–∏—Å—Ç–µ–º–µ base
NK_val = N * k # —ç—Ç–∞–ª–æ–Ω–Ω–æ–µ —á–∏—Å–ª–æ (N*k) –∫–∞–∫ —Ü–µ–ª–æ–µ
NK_str = string_in_base(NK_val, base) # —ç—Ç–∞–ª–æ–Ω–Ω–æ–µ —á–∏—Å–ª–æ –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞ –≤ —Å–∏—Å—Ç–µ–º–µ base

parts = split_number_str_base(N_str, m) # —Ä–∞–∑–±–∏–≤–∞–µ–º N –Ω–∞ m —á–∞—Å—Ç–µ–π
multiplied_parts = [multiply_preserve_length_base(p, k, base) for p in parts] # —É–º–Ω–æ–∂–∞–µ–º –∫–∞–∂–¥—É—é —á–∞—Å—Ç—å –Ω–∞ k –≤ —Å–∏—Å—Ç–µ–º–µ base
PQ_str = join(multiplied_parts) # —Å–∫–ª–µ–∏–≤–∞–µ–º —á–∞—Å—Ç–∏ –æ–±—Ä–∞—Ç–Ω–æ –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É

PQ_clean = remove_leading_zeros(PQ_str) # —É–±–∏—Ä–∞–µ–º –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ –∏–∑ PQ
NK_clean = remove_leading_zeros(NK_str) # —É–±–∏—Ä–∞–µ–º –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ –∏–∑ NK

result = compare_pq_nk(PQ_clean, NK_clean) # —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º –∏ –ø–æ–ª—É—á–∞–µ–º —Ç–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è

return ( # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏
