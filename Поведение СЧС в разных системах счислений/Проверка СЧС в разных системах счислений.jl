m = m, # —á–∏—Å–ª–æ —á–∞—Å—Ç–µ–π
k = k, # –º–Ω–æ–∂–∏—Ç–µ–ª—å
N_str = N_str, # N –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞
parts = string(parts), # —á–∞—Å—Ç–∏ –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞ (–¥–ª—è CSV)
multiplied_parts = string(multiplied_parts), # —É–º–Ω–æ–∂–µ–Ω–Ω—ã–µ —á–∞—Å—Ç–∏ –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞
PQ = PQ_clean, # —Ä–µ–∑—É–ª—å—Ç–∞—Ç (PQ)
NK = NK_clean, # —ç—Ç–∞–ª–æ–Ω (NK)
result = result # —Ç–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
    ) # –∫–æ–Ω–µ—Ü –≤–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä—ã
end # –∫–æ–Ω–µ—Ü —Ñ—É–Ω–∫—Ü–∏–∏

