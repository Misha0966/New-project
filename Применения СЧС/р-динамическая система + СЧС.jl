"–°–û–í–ü–ê–î–ê–ï–¢ –¢–û–õ–¨–ö–û –ö–û–ù–ï–¶"     => :coral,    # –¶–≤–µ—Ç –¥–ª—è —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è —Ç–æ–ª—å–∫–æ –∫–æ–Ω—Ü–∞
