using Printf          # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ –≤—ã–≤–æ–¥–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, @sprintf)
using CSV             # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å CSV-—Ñ–∞–π–ª–∞–º–∏
using DataFrames      # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —Ç–∞–±–ª–∏—á–Ω—ã–º–∏ –¥–∞–Ω–Ω—ã–º–∏ (DataFrame)
using Plots           # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –≥—Ä–∞—Ñ–∏–∫–æ–≤
using ProgressMeter   # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä–∞
using Base.Threads    # –ò–º–ø–æ—Ä—Ç –º–æ–¥—É–ª—è –¥–ª—è –º–Ω–æ–≥–æ–ø–æ—Ç–æ—á–Ω–æ—Å—Ç–∏

gr()                  # –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –±—ç–∫–µ–Ω–¥–∞ –≥—Ä–∞—Ñ–∏–∫–∏ Plots –≤ —Ä–µ–∂–∏–º GR (–±—ã—Å—Ç—Ä—ã–π –∏ –ª—ë–≥–∫–∏–π)

# –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ N –≤ —Å—Ç—Ä–æ–∫—É –¥–ª–∏–Ω—ã L –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è –ø–æ –æ—Å–Ω–æ–≤–∞–Ω–∏—é p,
# —É—á–∏—Ç—ã–≤–∞—è —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ L —Ü–∏—Ñ—Ä (–ø–æ –º–æ–¥—É–ª—é p^L)
function base_p_string(N::Integer, p::Integer, L::Integer)
N_mod = N % (p^L)                              # –ë–µ—Ä—ë–º N –ø–æ –º–æ–¥—É–ª—é p^L, —á—Ç–æ–±—ã –æ–≥—Ä–∞–Ω–∏—á–∏—Ç—å –¥–ª–∏–Ω—É
digits_p = digits(N_mod, base = p, pad = L)    # –ü–æ–ª—É—á–∞–µ–º —Ü–∏—Ñ—Ä—ã —á–∏—Å–ª–∞ –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è p, –¥–æ–ø–æ–ª–Ω—è—è –Ω—É–ª—è–º–∏ –¥–æ –¥–ª–∏–Ω—ã L
return join(reverse(digits_p))                 # –†–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –ø–æ—Ä—è–¥–æ–∫ (digits –¥–∞—ë—Ç –º–ª–∞–¥—à–∏–µ —Ä–∞–∑—Ä—è–¥—ã –ø–µ—Ä–≤—ã–º–∏) –∏ —Å–∫–ª–µ–∏–≤–∞–µ–º –≤ —Å—Ç—Ä–æ–∫—É
end

# –†–∞–∑–±–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫—É s –Ω–∞ m —á–∞—Å—Ç–µ–π –ø—Ä–∏–º–µ—Ä–Ω–æ —Ä–∞–≤–Ω–æ–π –¥–ª–∏–Ω—ã (–ø–µ—Ä–≤—ã–µ —á–∞—Å—Ç–∏ –º–æ–≥—É—Ç –±—ã—Ç—å –¥–ª–∏–Ω–Ω–µ–µ –Ω–∞ 1 —Å–∏–º–≤–æ–ª –ø—Ä–∏ –æ—Å—Ç–∞—Ç–∫–µ)
function split_string_equal(s::String, m::Integer)
len = length(s)                                # –û–±—â–∞—è –¥–ª–∏–Ω–∞ —Å—Ç—Ä–æ–∫–∏
base_len = div(len, m)                         # –ë–∞–∑–æ–≤–∞—è –¥–ª–∏–Ω–∞ –∫–∞–∂–¥–æ–π —á–∞—Å—Ç–∏ (—Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –¥–µ–ª–µ–Ω–∏–µ)
remainder = len % m                            # –û—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è ‚Äî —Å–∫–æ–ª—å–∫–æ —á–∞—Å—Ç–µ–π –ø–æ–ª—É—á–∞—Ç +1 —Å–∏–º–≤–æ–ª
parts = String[]                               # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—É—Å—Ç–æ–≥–æ –º–∞—Å—Å–∏–≤–∞ –¥–ª—è —á–∞—Å—Ç–µ–π
idx = 1                                        # –ù–∞—á–∞–ª—å–Ω—ã–π –∏–Ω–¥–µ–∫—Å –≤ —Å—Ç—Ä–æ–∫–µ
for i in 1:m                                   # –¶–∏–∫–ª –ø–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤—É —á–∞—Å—Ç–µ–π
current_len = base_len + (i <= remainder ? 1 : 0)  # –¢–µ–∫—É—â–∞—è –¥–ª–∏–Ω–∞: +1, –µ—Å–ª–∏ i ‚â§ –æ—Å—Ç–∞—Ç–∫–∞
push!(parts, s[idx:idx+current_len-1])     # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥—Å—Ç—Ä–æ–∫—É –≤ –º–∞—Å—Å–∏–≤ —á–∞—Å—Ç–µ–π
idx += current_len                         # –°–¥–≤–∏–≥–∞–µ–º –∏–Ω–¥–µ–∫—Å –Ω–∞ –¥–ª–∏–Ω—É —Ç–µ–∫—É—â–µ–π —á–∞—Å—Ç–∏
end
return parts                                # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –º–∞—Å—Å–∏–≤ —á–∞—Å—Ç–µ–π
end

# –£–º–Ω–æ–∂–∞–µ—Ç —á–∞—Å—Ç—å —á–∏—Å–ª–∞ (–∫–∞–∫ —Å—Ç—Ä–æ–∫—É –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è p) –Ω–∞ k, —Å–æ—Ö—Ä–∞–Ω—è—è –¥–ª–∏–Ω—É —á–∞—Å—Ç–∏
function multiply_preserve_length_p(part::String, k::Integer, p::Integer)
if all(c == '0' for c in part)               # –ï—Å–ª–∏ –≤—Å—è —á–∞—Å—Ç—å —Å–æ—Å—Ç–æ–∏—Ç –∏–∑ –Ω—É–ª–µ–π
num = 0                                      # –ß–∏—Å–ª–æ —Ä–∞–≤–Ω–æ –Ω—É–ª—é
else
num = parse(BigInt, part, base = p)           # –ò–Ω–∞—á–µ –ø–∞—Ä—Å–∏–º —Å—Ç—Ä–æ–∫—É –∫–∞–∫ —á–∏—Å–ª–æ –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è p
end
prod = (num * k) % (p^length(part))            # –£–º–Ω–æ–∂–∞–µ–º –∏ –±–µ—Ä—ë–º –ø–æ –º–æ–¥—É–ª—é p^(–¥–ª–∏–Ω–∞ —á–∞—Å—Ç–∏), —á—Ç–æ–±—ã –Ω–µ –≤—ã–π—Ç–∏ –∑–∞ —Ä–∞–∑—Ä—è–¥–Ω–æ—Å—Ç—å
res_str = string(prod, base = p)               # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É –≤ —Å–∏—Å—Ç–µ–º–µ p
return lpad(res_str, length(part), '0')        # –î–æ–ø–æ–ª–Ω—è–µ–º —Å–ª–µ–≤–∞ –Ω—É–ª—è–º–∏ –¥–æ –∏—Å—Ö–æ–¥–Ω–æ–π –¥–ª–∏–Ω—ã —á–∞—Å—Ç–∏
end

# –£–¥–∞–ª—è–µ—Ç –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ –∏–∑ —Å—Ç—Ä–æ–∫–∏, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–µ–π —á–∏—Å–ª–æ –≤ —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è p;
# –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ —Å–æ—Å—Ç–æ–∏—Ç —Ç–æ–ª—å–∫–æ –∏–∑ –Ω—É–ª–µ–π ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç "0"
function remove_leading_zeros_p(s::String)
if all(c == '0' for c in s)                # –ï—Å–ª–∏ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã ‚Äî –Ω—É–ª–∏
return "0"                                 # –í–æ–∑–≤—Ä–∞—â–∞–µ–º "0"
else
idx = findfirst(c -> c != '0', s)          # –ù–∞—Ö–æ–¥–∏–º –ø–µ—Ä–≤—ã–π –Ω–µ–Ω—É–ª–µ–≤–æ–π —Å–∏–º–≤–æ–ª
return s[idx:end]                          # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–æ–¥—Å—Ç—Ä–æ–∫—É –æ—Ç –Ω–µ–≥–æ –¥–æ –∫–æ–Ω—Ü–∞
end
end

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç –¥–≤–µ —Å—Ç—Ä–æ–∫–∏ PQ –∏ NK –∏ –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç —Ç–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è:
# –ø–æ–ª–Ω–æ–µ, —Ç–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ, —Ç–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü, –Ω–∞—á–∞–ª–æ+–∫–æ–Ω–µ—Ü –∏–ª–∏ –Ω–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
function compare_pq_nk(pq::String, nk::String)
if pq == nk                                # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∏ –ø–æ–ª–Ω–æ—Å—Ç—å—é —Å–æ–≤–ø–∞–¥–∞—é—Ç
return "–ü–û–õ–ù–û–ï –°–û–í–ü–ê–î–ï–ù–ò–ï"                 # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –º–µ—Ç–∫—É
end
min_len = min(length(pq), length(nk))      # –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ –∏–∑ –¥–≤—É—Ö —Å—Ç—Ä–æ–∫
prefix = 0                                 # –°—á—ë—Ç—á–∏–∫ —Å–æ–≤–ø–∞–¥–∞—é—â–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ —Å –Ω–∞—á–∞–ª–∞
for i in 1:min_len                         # –¶–∏–∫–ª –ø–æ –ø–æ–∑–∏—Ü–∏—è–º —Å –Ω–∞—á–∞–ª–∞
pq[i] == nk[i] ? prefix += 1 : break       # –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª—ã —Å–æ–≤–ø–∞–¥–∞—é—Ç ‚Äî —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫, –∏–Ω–∞—á–µ –≤—ã—Ö–æ–¥–∏–º
end
suffix = 0                                 # –°—á—ë—Ç—á–∏–∫ —Å–æ–≤–ø–∞–¥–∞—é—â–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ —Å –∫–æ–Ω—Ü–∞
for i in 1:min_len                         # –¶–∏–∫–ª –ø–æ –ø–æ–∑–∏—Ü–∏—è–º —Å –∫–æ–Ω—Ü–∞
pq[end-i+1] == nk[end-i+1] ? suffix += 1 : break  # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å –∫–æ–Ω—Ü–∞; –µ—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞—é—Ç ‚Äî —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º, –∏–Ω–∞—á–µ –≤—ã—Ö–æ–¥–∏–º
end
if prefix > 0 && suffix > 0                # –ï—Å–ª–∏ –µ—Å—Ç—å —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è –∏ –≤ –Ω–∞—á–∞–ª–µ, –∏ –≤ –∫–æ–Ω—Ü–µ
return "–°–û–í–ü–ê–î–ê–Æ–¢ –ù–ê–ß–ê–õ–û –ò –ö–û–ù–ï–¶"          # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –º–µ—Ç–∫—É
elseif prefix > 0                          # –ï—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ
return "–°–û–í–ü–ê–î–ê–ï–¢ –¢–û–õ–¨–ö–û –ù–ê–ß–ê–õ–û"           # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –º–µ—Ç–∫—É
elseif suffix > 0                          # –ï—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Ç–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü
return "–°–û–í–ü–ê–î–ê–ï–¢ –¢–û–õ–¨–ö–û –ö–û–ù–ï–¶"            # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –º–µ—Ç–∫—É
else                                       # –ï—Å–ª–∏ –Ω–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π –Ω–∏ –≤ –Ω–∞—á–∞–ª–µ, –Ω–∏ –≤ –∫–æ–Ω—Ü–µ
return "–ù–ï–¢ –°–û–í–ü–ê–î–ï–ù–ò–ô"                    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é –º–µ—Ç–∫—É
end
end

# –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –∫–∞–¥—Ä–æ–≤ –∞–Ω–∏–º–∞—Ü–∏–∏; m –ø–µ—Ä–µ–¥–∞—ë—Ç—Å—è —è–≤–Ω–æ

# –°–æ–∑–¥–∞—ë—Ç –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç –æ–¥–∏–Ω –∫–∞–¥—Ä –≤–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏–∏ –°–ß–°
function plot_sns_frame(N, parts, mult_parts, PQ, NK, result, p, L, k, m, frame_dir, frame_id)
fig = plot(                                # –°–æ–∑–¥–∞—ë–º –º–Ω–æ–≥–æ—Å–ª–æ–π–Ω—ã–π –≥—Ä–∞—Ñ–∏–∫ (4 —Å—Ç—Ä–æ–∫–∏, 1 —Å—Ç–æ–ª–±–µ—Ü)
title = "–°–ß–° | p = $p | N = $N | k = $k | L = $L",  # –ó–∞–≥–æ–ª–æ–≤–æ–∫ –≤—Å–µ–π —Ñ–∏–≥—É—Ä—ã
size = (1200, 700),                        # –†–∞–∑–º–µ—Ä –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ –ø–∏–∫—Å–µ–ª—è—Ö
layout = (4, 1),                           # –†–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏–µ: 4 –ø–æ–¥–≥—Ä–∞—Ñ–∏–∫–∞ –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ
top_margin = 10Plots.mm,                   # –û—Ç—Å—Ç—É–ø —Å–≤–µ—Ä—Ö—É
bottom_margin = 10Plots.mm,                # –û—Ç—Å—Ç—É–ø —Å–Ω–∏–∑—É
titlefontsize = 12,                        # –†–∞–∑–º–µ—Ä —à—Ä–∏—Ñ—Ç–∞ –∑–∞–≥–æ–ª–æ–≤–∫–∞
grid = false                               # –ë–µ–∑ —Å–µ—Ç–∫–∏
)

# N –≤ p-–∏—á–Ω–æ–π –∑–∞–ø–∏—Å–∏
N_str = base_p_string(N, p, L)                 # –ü–æ–ª—É—á–∞–µ–º —Å—Ç—Ä–æ–∫–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ N –≤ —Å–∏—Å—Ç–µ–º–µ p
plot!(fig[1], xlims = (0.5, L + 0.5), yticks = ([], []), xticks = (1:L, collect(N_str)),  # –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ—Å–µ–π
seriestype = :scatter, markersize = 0, label = "")  # –ü—É—Å—Ç–æ–π scatter –¥–ª—è —Ä–∞–∑–º–µ—â–µ–Ω–∏—è –∞–Ω–Ω–æ—Ç–∞—Ü–∏–π
for i in 1:L                                   # –¶–∏–∫–ª –ø–æ –∫–∞–∂–¥–æ–π —Ü–∏—Ñ—Ä–µ
annotate!(fig[1], (i, 0.5, text(string(N_str[i]), :center, 12)))  # –ü–æ–¥–ø–∏—Å—ã–≤–∞–µ–º –∫–∞–∂–¥—É—é —Ü–∏—Ñ—Ä—É
end
plot!(fig[1], title = "N –≤ $p-–∏—á–Ω–æ–π –∑–∞–ø–∏—Å–∏ (–¥–ª–∏–Ω–∞ L = $L)")  # –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø–æ–¥–≥—Ä–∞—Ñ–∏–∫–∞

# –†–∞–∑–±–∏–µ–Ω–∏–µ –Ω–∞ m —á–∞—Å—Ç–µ–π
y_pos = 1:length(parts)                        # –ü–æ–∑–∏—Ü–∏–∏ –ø–æ –≤–µ—Ä—Ç–∏–∫–∞–ª–∏ –¥–ª—è –∫–∞–∂–¥–æ–π —á–∞—Å—Ç–∏
plot!(fig[2], yticks = (y_pos, ["–ß–∞—Å—Ç—å $i" for i in y_pos]), xticks = ([], []),  # –ú–µ—Ç–∫–∏ –ø–æ Y: "–ß–∞—Å—Ç—å 1", "–ß–∞—Å—Ç—å 2"...
seriestype = :scatter, markersize = 0, label = "")  # –ü—É—Å—Ç–æ–π –≥—Ä–∞—Ñ–∏–∫ –¥–ª—è –∞–Ω–Ω–æ—Ç–∞—Ü–∏–π
for (i, part) in enumerate(parts)               # –¶–∏–∫–ª –ø–æ —á–∞—Å—Ç—è–º
annotate!(fig[2], (0, i, text(part, :center, 12)))  # –í—ã–≤–æ–¥–∏–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ —á–∞—Å—Ç–∏
end
plot!(fig[2], title = "–†–∞–∑–±–∏–µ–Ω–∏–µ –Ω–∞ $m —á–∞—Å—Ç–µ–π")  # –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø–æ–¥–≥—Ä–∞—Ñ–∏–∫–∞

# –†–µ–∑—É–ª—å—Ç–∞—Ç –ª–æ–∫–∞–ª—å–Ω–æ–≥–æ —É–º–Ω–æ–∂–µ–Ω–∏—è –∫–∞–∂–¥–æ–π —á–∞—Å—Ç–∏ –Ω–∞ k
plot!(fig[3], yticks = (1:length(mult_parts), ["√ók" for _ in mult_parts]), xticks = ([], []),  # –ú–µ—Ç–∫–∏ "√ók"
seriestype = :scatter, markersize = 0, label = "")  # –ü—É—Å—Ç–æ–π –≥—Ä–∞—Ñ–∏–∫
for (i, mp) in enumerate(mult_parts)           # –¶–∏–∫–ª –ø–æ —É–º–Ω–æ–∂–µ–Ω–Ω—ã–º —á–∞—Å—Ç—è–º
