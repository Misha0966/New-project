)
bar_color = get(color_map, result, :black)     # –ü–æ–ª—É—á–∞–µ–º —Ü–≤–µ—Ç –ø–æ —Ç–∏–ø—É —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è; —á—ë—Ä–Ω—ã–π ‚Äî –µ—Å–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ
bar!(fig[4], [1, 2], [1.0, 1.0], color = [bar_color, :purple], label = "",  # –°—Ç–æ–ª–±—Ü—ã: PQ (—Ü–≤–µ—Ç –∑–∞–≤–∏—Å–∏—Ç –æ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞), NK ‚Äî —Ñ–∏–æ–ª–µ—Ç–æ–≤—ã–π
xticks = ([1, 2], ["PQ", "NK"]))          # –ú–µ—Ç–∫–∏ –ø–æ X: "PQ", "NK"
annotate!(fig[4], (1, 1.15, text(PQ, :center, 11)))  # –ü–æ–¥–ø–∏—Å—å –∑–Ω–∞—á–µ–Ω–∏—è PQ –Ω–∞–¥ —Å—Ç–æ–ª–±—Ü–æ–º
annotate!(fig[4], (2, 1.15, text(NK, :center, 11)))  # –ü–æ–¥–ø–∏—Å—å –∑–Ω–∞—á–µ–Ω–∏—è NK –Ω–∞–¥ —Å—Ç–æ–ª–±—Ü–æ–º
annotate!(fig[4], (1.5, 0.5, text(result, :center, 12, bar_color)))  # –ü–æ–¥–ø–∏—Å—å —Ç–∏–ø–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è –º–µ–∂–¥—É —Å—Ç–æ–ª–±—Ü–∞–º–∏
plot!(fig[4], yticks = ([], []), title = "–°—Ä–∞–≤–Ω–µ–Ω–∏–µ: –≥–ª–æ–±–∞–ª—å–Ω–æ–µ vs –ª–æ–∫–∞–ª—å–Ω–æ–µ")  # –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø–æ–¥–≥—Ä–∞—Ñ–∏–∫–∞

savefig(fig, joinpath(frame_dir, @sprintf "frame_%06d.png" frame_id))  # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∫–∞–¥—Ä —Å –Ω—É–º–µ—Ä–∞—Ü–∏–µ–π 000001.png –∏ —Ç.–¥.
end

# –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∞–Ω–∞–ª–∏–∑–∞ –°–ß–°

# –í—ã–ø–æ–ª–Ω—è–µ—Ç –∞–Ω–∞–ª–∏–∑ –°–ß–° –¥–ª—è –¥–∏–∞–ø–∞–∑–æ–Ω–∞ —á–∏—Å–µ–ª [start_N, stop_N] –≤ p-–∞–¥–∏—á–µ—Å–∫–æ–π —Å–∏—Å—Ç–µ–º–µ
function run_sns_p_adic_video(start_N::Integer, stop_N::Integer, m::Integer, k::Integer, p::Integer, L::Integer)
mkpath("frames")                           # –°–æ–∑–¥–∞—ë–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é "frames", –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
results_df = DataFrame(                    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Ç–∞–±–ª–∏—Ü—ã —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
N = Int[],                                 # –ò—Å—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ N
m = Int[],                                 # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å—Ç–µ–π
k = Int[],                                 # –ú–Ω–æ–∂–∏—Ç–µ–ª—å
p = Int[],                                 # –û—Å–Ω–æ–≤–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã —Å—á–∏—Å–ª–µ–Ω–∏—è
L = Int[],                                 # –î–ª–∏–Ω–∞ —á–∏—Å–ª–∞ (–≤ —Ü–∏—Ñ—Ä–∞—Ö)
parts = String[],                          # –ò—Å—Ö–æ–¥–Ω—ã–µ —á–∞—Å—Ç–∏ —á–∏—Å–ª–∞
multiplied_parts = String[],               # –ß–∞—Å—Ç–∏ –ø–æ—Å–ª–µ —É–º–Ω–æ–∂–µ–Ω–∏—è
PQ = String[],                             # –†–µ–∑—É–ª—å—Ç–∞—Ç –ª–æ–∫–∞–ª—å–Ω–æ–≥–æ —É–º–Ω–æ–∂–µ–Ω–∏—è (—Å–∫–ª–µ–µ–Ω–Ω—ã–π)
NK = String[],                             # –†–µ–∑—É–ª—å—Ç–∞—Ç –≥–ª–æ–±–∞–ª—å–Ω–æ–≥–æ —É–º–Ω–æ–∂–µ–Ω–∏—è
result = String[]                          # –¢–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
)

count_full = Atomic{Int}(0)                 # –ê—Ç–æ–º–∞—Ä–Ω—ã–π —Å—á—ë—Ç—á–∏–∫ –¥–ª—è –ø–æ—Ç–æ–∫–æ–±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏: –ø–æ–ª–Ω—ã–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
count_both = Atomic{Int}(0)                 # –°–æ–≤–ø–∞–¥–µ–Ω–∏—è –Ω–∞—á–∞–ª–∞ –∏ –∫–æ–Ω—Ü–∞
count_start = Atomic{Int}(0)                # –¢–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ
count_end = Atomic{Int}(0)                  # –¢–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü
count_none = Atomic{Int}(0)                 # –ù–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π

total = stop_N - start_N + 1               # –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∏—Å–µ–ª –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ
@showprogress "Running SNS analysis" for (idx, N) in enumerate(start_N:stop_N)  # –ü—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä + —Ü–∏–∫–ª –ø–æ —á–∏—Å–ª–∞–º
N_p = N % (p^L)                            # –ü—Ä–∏–≤–æ–¥–∏–º N –∫ –¥–∏–∞–ø–∞–∑–æ–Ω—É [0, p^L - 1]
N_str = base_p_string(N_p, p, L)           # –ü–æ–ª—É—á–∞–µ–º —Å—Ç—Ä–æ–∫—É –¥–ª–∏–Ω—ã L –≤ —Å–∏—Å—Ç–µ–º–µ p

parts = split_string_equal(N_str, m)       # –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ m —á–∞—Å—Ç–µ–π
mult_parts = [multiply_preserve_length_p(part, k, p) for part in parts]  # –£–º–Ω–æ–∂–∞–µ–º –∫–∞–∂–¥—É—é —á–∞—Å—Ç—å –Ω–∞ k, —Å–æ—Ö—Ä–∞–Ω—è—è –¥–ª–∏–Ω—É

PQ_raw = join(mult_parts)                  # –°–∫–ª–µ–∏–≤–∞–µ–º —É–º–Ω–æ–∂–µ–Ω–Ω—ã–µ —á–∞—Å—Ç–∏ ‚Üí PQ (–ª–æ–∫–∞–ª—å–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç)
NK_raw = string((N_p * k) % (p^L), base = p)  # –ì–ª–æ–±–∞–ª—å–Ω–æ–µ —É–º–Ω–æ–∂–µ–Ω–∏–µ: (N * k) mod p^L ‚Üí —Å—Ç—Ä–æ–∫–∞ –≤ —Å–∏—Å—Ç–µ–º–µ p

PQ = remove_leading_zeros_p(PQ_raw)        # –£–¥–∞–ª—è–µ–º –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ —É PQ
NK = remove_leading_zeros_p(NK_raw)        # –£–¥–∞–ª—è–µ–º –≤–µ–¥—É—â–∏–µ –Ω—É–ª–∏ —É NK

result = compare_pq_nk(PQ, NK)             # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º PQ –∏ NK, –æ–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è

if result == "–ü–û–õ–ù–û–ï –°–û–í–ü–ê–î–ï–ù–ò–ï"           # –ï—Å–ª–∏ –ø–æ–ª–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
Threads.atomic_add!(count_full, 1)         # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π —Å—á—ë—Ç—á–∏–∫ –∞—Ç–æ–º–∞—Ä–Ω–æ
elseif result == "–°–û–í–ü–ê–î–ê–Æ–¢ –ù–ê–ß–ê–õ–û –ò –ö–û–ù–ï–¶"  # –ï—Å–ª–∏ —Å–æ–≤–ø–∞–¥–∞—é—Ç –Ω–∞—á–∞–ª–æ –∏ –∫–æ–Ω–µ—Ü
Threads.atomic_add!(count_both, 1)           # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫
elseif result == "–°–û–í–ü–ê–î–ê–ï–¢ –¢–û–õ–¨–ö–û –ù–ê–ß–ê–õ–û"   # –¢–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–æ
Threads.atomic_add!(count_start, 1)          # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫
elseif result == "–°–û–í–ü–ê–î–ê–ï–¢ –¢–û–õ–¨–ö–û –ö–û–ù–ï–¶"    # –¢–æ–ª—å–∫–æ –∫–æ–Ω–µ—Ü
Threads.atomic_add!(count_end, 1)            # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫
else                                         # –ù–µ—Ç —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
Threads.atomic_add!(count_none, 1)           # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫
end

push!(results_df, (                          # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Ç–∞–±–ª–∏—Ü—É —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
N = N,                                       # –¢–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ N
m = m,                                       # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å—Ç–µ–π (—Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–æ)
k = k,                                       # –ú–Ω–æ–∂–∏—Ç–µ–ª—å (—Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω)
p = p,                                       # –û—Å–Ω–æ–≤–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã (—Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–æ)
L = L,                                       # –î–ª–∏–Ω–∞ —á–∏—Å–ª–∞ (—Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–∞)
parts = string(parts),                       # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –º–∞—Å—Å–∏–≤ —á–∞—Å—Ç–µ–π –≤ —Å—Ç—Ä–æ–∫—É –¥–ª—è CSV
multiplied_parts = string(mult_parts),       # –¢–æ –∂–µ –¥–ª—è —É–º–Ω–æ–∂–µ–Ω–Ω—ã—Ö —á–∞—Å—Ç–µ–π
PQ = PQ,                                     # –õ–æ–∫–∞–ª—å–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –±–µ–∑ –≤–µ–¥—É—â–∏—Ö –Ω—É–ª–µ–π
NK = NK,                                     # –ì–ª–æ–±–∞–ª—å–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –±–µ–∑ –≤–µ–¥—É—â–∏—Ö –Ω—É–ª–µ–π
result = result                              # –¢–∏–ø —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
))

if total <= 5000                             # –ï—Å–ª–∏ –¥–∏–∞–ø–∞–∑–æ–Ω –Ω–µ–±–æ–ª—å—à–æ–π ‚Äî –≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–∞–¥—Ä—ã –¥–ª—è –≤–∏–¥–µ–æ
plot_sns_frame(N, parts, mult_parts, PQ, NK, result, p, L, k, m, "frames", idx)  # –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –æ–¥–Ω–æ–≥–æ –∫–∞–¥—Ä–∞
end
end

CSV.write("results_sns_p_adic.csv", results_df)  # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Å—é —Ç–∞–±–ª–∏—Ü—É –≤ CSV-—Ñ–∞–π–ª

full = count_full[]                            # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ª–Ω—ã—Ö —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
both = count_both[]                            # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π –Ω–∞—á–∞–ª–∞ –∏ –∫–æ–Ω—Ü–∞
start = count_start[]                          # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π —Ç–æ–ª—å–∫–æ –Ω–∞—á–∞–ª–∞
ennd = count_end[]                             # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π —Ç–æ–ª—å–∫–æ –∫–æ–Ω—Ü–∞ (–ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è ennd –∏–∑-–∑–∞ –∑–∞—Ä–µ–∑–µ—Ä–≤–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ —Å–ª–æ–≤–∞ end)
none = count_none[]                            # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–ª—É—á–∞–µ–≤ –±–µ–∑ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π

open("statistics_sns_p_adic.txt", "w") do io   # –û—Ç–∫—Ä—ã–≤–∞–µ–º —Ñ–∞–π–ª –¥–ª—è –∑–∞–ø–∏—Å–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
write(io, "–°—Ç—Ä—É–∫—Ç—É—Ä–Ω–∞—è –ß–∏—Å–ª–æ–≤–∞—è –°–∏–º–º–µ—Ç—Ä–∏—è (–°–ß–°) ‚Äî p-–∞–¥–∏—á–µ—Å–∫–∞—è —Å–∏—Å—Ç–µ–º–∞\n")  # –ó–∞–ø–∏—Å—å –∑–∞–≥–æ–ª–æ–≤–∫–∞ –æ—Ç—á—ë—Ç–∞
write(io, "==========================================================\n")  # –ó–∞–ø–∏—Å—å —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å–Ω–æ–π –ª–∏–Ω–∏–∏
write(io, "–î–∏–∞–ø–∞–∑–æ–Ω N: [$start_N, $stop_N]\n")  # –ó–∞–ø–∏—Å—å –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º–æ–≥–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ —á–∏—Å–µ–ª
write(io, "–û—Å–Ω–æ–≤–∞–Ω–∏–µ p = $p, –¥–ª–∏–Ω–∞ L = $L\n")   # –ó–∞–ø–∏—Å—å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ p-–∏—á–Ω–æ–π —Å–∏—Å—Ç–µ–º—ã
write(io, "–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å—Ç–µ–π m = $m, –º–Ω–æ–∂–∏—Ç–µ–ª—å k = $k\n")  # –ó–∞–ø–∏—Å—å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ —Ä–∞–∑–±–∏–µ–Ω–∏—è –∏ —É–º–Ω–æ–∂–µ–Ω–∏—è
